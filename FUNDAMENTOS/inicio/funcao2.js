

const imprimirSoma = function (a, b) {
    return a + b
}

console.log(imprimirSoma(2, 3)) // 5

// Função arrow (função de seta)

const subtracao = (a, b) => {
    return a - b
}

console.log(subtracao(5, 2)) // 3

// Arrow function com retorno implícito (sem chaves)

const multiplicacao = (a, b) => a * b

console.log(multiplicacao(3, 4)) // 12